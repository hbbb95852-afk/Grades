"""
برنامج تقدير درجة الطالب
يقبل إدخال درجة من المستخدم (0-100) ويعرض التقدير: ضعيف، جيد، جيد جدًا، ممتاز
يستمر حتى يدخل المستخدم كلمة 'خروج'
يتحمل الأخطاء: قيم غير رقمية، أرقام خارج النطاق
"""

def تقدير_الدرجة(الدرجة):
    """إرجاع التقدير المناسب بناءً على الدرجة"""
    if الدرجة >= 90:
        return "ممتاز"
    elif الدرجة >= 80:
        return "جيد جدًا"
    elif الدرجة >= 70:
        return "جيد"
    elif الدرجة >= 60:
        return "مقبول"  # يمكن تغييره حسب رغبتك، أو جعله ضعيف
    else:
        return "ضعيف"

def main():
    print("=" * 40)
    print("برنامج تقدير درجات الطلاب")
    print("أدخل درجة الطالب (0-100) أو اكتب 'خروج' لإنهاء البرنامج")
    print("=" * 40)

    while True:
        # إدخال المستخدم
        user_input = input("\nأدخل الدرجة أو 'خروج': ").strip()

        # شرط الخروج
        if user_input == "خروج":
            print("تم إنهاء البرنامج. شكرًا لاستخدامك البرنامج.")
            break

        # التحقق من صحة الإدخال (رقمي)
        try:
            الدرجة = float(user_input)
        except ValueError:
            print("خطأ: يجب إدخال رقم صحيح أو عشري بين 0 و100، أو كلمة 'خروج'.")
            continue

        # التحقق من النطاق
        if الدرجة < 0 or الدرجة > 100:
            print("خطأ: الدرجة يجب أن تكون بين 0 و100 فقط.")
            continue

        # حساب التقدير وعرضه
        التقدير = تقدير_الدرجة(الدرجة)
        print(f"الدرجة: {الدرجة} --> التقدير: {التقدير}")

# تشغيل البرنامج
if __name__ == "__main__":
    main()
